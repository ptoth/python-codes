# str
x = "Hello World"

# int
x = 20

# float
x = 20.5

# complex
x = 1j

# list
x = ["apple", "banana", "cherry"]

# tuple (unchangeable list)
x = ("apple", "banana", "cherry")

# range of number from 0 to maxValue-1
x = range(6) # 0,1,2,3,4,5 but not 6

# dictionary
x = {
        "name" : "John", 
        "age" : 36
    }

# set
x = {"apple", "banana", "cherry"}

# frozen set (unchangeable set)
x = frozenset({"apple", "banana", "cherry"})

# bool/boolean
x = True
